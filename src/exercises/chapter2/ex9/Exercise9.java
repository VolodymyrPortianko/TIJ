public class Exercise9 {
    public static void main(String[] args) {
        Byte bb = 7;
        byte b = bb;
        System.out.println(bb + " = " + b);
        Short sb = 100;
        short s = sb;
        System.out.println(sb + " = " + s);
        Character cb = 'a';
        char c = cb;
        System.out.println(cb + " = " + c);
        Integer ib = 2000000;
        int i = ib;
        System.out.println(ib + " = " + i);
        Long lb = 9000000000L;
        long l = lb;
        System.out.println(lb + " = " + l);
        Float fb = 1.5f;
        float f = fb;
        System.out.println(fb + " = " + f);
        Double db = 7.8;
        double d = db;
        System.out.println(db + " = " + d);
        Boolean bob = true;
        boolean bo = bob;
        System.out.println(bob + " = " + bo);
    }
}
